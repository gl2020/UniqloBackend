--  DDL for View V_ALL_SECTIONS
--------------------------------------------------------

  CREATE OR REPLACE FORCE EDITIONABLE VIEW "ADMIN"."V_ALL_SECTIONS" ("MAIN_SECTION", "CATEGORY", "SUBCATEGORY", "ITEM", "ORIGINAL_PRICE", "DISCOUNT_PRICE", "ITEM_LABEL", "ITEM_COLLECTION", "ITEM_IMAGE", "CUSTOMIZE_MAIN_SECTION_ORDER", "CUSTOMIZE_CATEGORY_ORDER", "CUSTOMIZE_SUBCATEGORY_ORDER") DEFAULT COLLATION "USING_NLS_COMP"  AS 
  SELECT DISTINCT ms.main_section,cat.category,scat.subcategory,i.item,i.original_price,discount_price,
        il.item_label,ic.item_collection, i.item_image,
        ms.customize_main_section_order,cat.customize_category_order ,scat.customize_subcategory_order
 FROM  main_section ms   
 INNER JOIN category cat ON ms.main_section_id=cat.main_section_id
 INNER JOIN Subcategory scat On cat.category_id=scat.category_id
 Left JOIN Subcategory_Item scati On  scat.subcategory_id = scati.subcategory_id                                  
 Left Join item i on scati.item_id=i.item_id
 Left Join item_label il on i.item_label_id= il.item_label_id
 Left Join item_collection ic on i.item_collection_id=ic.item_collection_id
 ORDER BY  ms.customize_main_section_order,cat.customize_category_order ,scat.customize_subcategory_order
;
